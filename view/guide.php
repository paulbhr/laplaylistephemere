  <h1>À Propos !</h1>
  <p>
    Web-radio intelligente et playlist interactive, La Playlist Éphémère est un concept réfléchi autour de la relation entre curateurs et leur audience, né d’une envie de partager du contenu sans l’imposer, et de permettre à l’utilisateur de n’avoir accès qu’aux données qui l’intéressent, le plus naturellement possible.
  </p><p>
    Ainsi, à chaque morceau est associé un nombre de points de vie, qui va évoluer en fonction des actions de l’utilisateur : a-t-il choisi de passer au morceau suivant avant la fin du premier? Est-il revenu sur le précédent? Chaque acte permet ainsi de mettre en avant ou en retrait les morceaux d’une même playlist, en fonction de l’utilisateur auquel celle-ci se trouve connectée.
  </p><p>
    C’est le principe de La Playlist Éphémère, faire d’une base de donnée fixe d’un côté serveur un outil vivant qui réponde au caractère humain de son destinataire.
  </p>
</section>

